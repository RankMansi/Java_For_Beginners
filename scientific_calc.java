import java.util.*;

class scientific_calc {
    public static void main(String[] args){
        System.out.println("The absolute value of -5 is:" + Math.abs(-17));
        System.out.println("The minimum value from 1 and 2 is:" + Math.min(7,17));
        System.out.println("The maximum value from 1 and 2 is:" + Math.max(1,2));
        System.out.println("The sine of 30deg is:" + Math.sin(30));
        System.out.println("The cosine of 60deg is:" + Math.cos(60));
        System.out.println("The tan of 45deg is:" + Math.tan(45));
        System.out.println("The natural logarithm of 22 is:" + Math.log(5));
        System.out.println("The logarithm of 22 with base 10 is:" + Math.log10(17));
        System.out.println("17.31 rounded to nearest integer is: " + Math.round(17.31)); 
    }
}
